/**
 * Round-trip tests for RFC-014 artifact ↔ file serialization.
 * Pure functions, no IO. Verifies that emitting an artifact to its file shape
 * and parsing it back preserves identity and all meaningful fields.
 */

import { test } from 'node:test'
import assert from 'node:assert/strict'
import {
  markdownArtifactToText,
  parseMarkdownArtifact,
  dataArtifactToSidecar,
  dataArtifactFromSidecar,
  paperToBibEntry,
  paperToSidecarEntry,
  parsePaperFile,
  parsePaperLibrary
} from '../artifact-files.js'
import { parseFrontMatter } from '../front-matter.js'
import type {
  NoteArtifact,
  WebContentArtifact,
  ToolOutputArtifact,
  DataArtifact,
  PaperArtifact,
  Provenance
} from '../../types.js'

const prov: Provenance = {
  source: 'agent',
  sessionId: 'sess-1',
  agentId: 'coordinator',
  extractedFrom: 'agent-response',
  messageId: 'msg-9'
}

test('note round-trips through markdown front-matter', () => {
  const note: NoteArtifact = {
    id: 'note-uuid-1',
    type: 'note',
    title: 'Threshold theorem',
    tags: ['surface-code', 'ecc'],
    summary: 'why d=3 tolerates one error',
    provenance: prov,
    createdAt: '2026-05-24T10:00:00.000Z',
    updatedAt: '2026-05-24T11:00:00.000Z',
    content: '# Body\n\nLine one.\n\n- bullet\n\nEnd.'
  }
  const text = markdownArtifactToText(note)
  const back = parseMarkdownArtifact(text) as NoteArtifact
  assert.equal(back.type, 'note')
  assert.deepEqual(back, note)
})

