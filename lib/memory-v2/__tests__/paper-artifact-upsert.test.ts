test('bit-identical re-upsert is a no-op write (still reports wasDeduped=true)', () => {
  const project = tmpProject()
  try {
    const first = upsertPaperArtifact(
      'Paper',
      {
        authors: ['Smith', 'Jones'],
        year: 2024,
        doi: '10.1234/x',
        abstract: 'abstract',
        venue: 'NeurIPS',
        tags: ['nlp']
      },
      ctx(project)
    )
    const updatedAtBefore = first.paper!.updatedAt

    // Sleep a hair to ensure any spurious write would shift updatedAt.
    const start = Date.now()
    while (Date.now() - start < 5) { /* spin */ }

    const second = upsertPaperArtifact(
      'Paper',
      {
        authors: ['Smith', 'Jones'],
        year: 2024,
        doi: '10.1234/x',
        abstract: 'abstract',
        venue: 'NeurIPS',
        tags: ['nlp']
      },
      ctx(project)
    )
    assert.equal(second.wasDeduped, true)
    // No-op means updatedAt unchanged (no artifactUpdate fired).
    const stored = getPaperById(project, first.paper!.id)
    assert.equal(stored.updatedAt, updatedAtBefore, 'updatedAt should not move on no-op')
  } finally {
    rmSync(project, { recursive: true, force: true })
  }
})
