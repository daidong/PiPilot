// ─── Fill-only regression tests — the core bug PR-0 fixes ─────────────────

test('REGRESSION: real DOI is NOT downgraded to unknown:* by a re-upsert lacking DOI', () => {
  const project = tmpProject()
  try {
    // First write: full data including real DOI.
    const first = upsertPaperArtifact(
      'Attention Is All You Need',
      {
        authors: ['Vaswani', 'Shazeer', 'Parmar', 'Uszkoreit', 'Jones'],
        year: 2017,
        doi: '10.48550/arxiv.1706.03762',
        citeKey: 'vaswani2017'
      },
      ctx(project)
    )

    // Second write: thinner data, no DOI. Simulates a stripped Zotero export
    // or a re-import of a partial .bib. Pre-fix this overwrote the DOI with
    // `unknown:vaswani2017`.
    const second = upsertPaperArtifact(
      'Attention Is All You Need',
      {
        authors: ['Vaswani, A.', 'others'],
        year: 2017,
        citeKey: 'vaswani2017'
        // no DOI
      },
      ctx(project)
    )

    assert.equal(second.wasDeduped, true)
    const stored = getPaperById(project, first.paper!.id)
    assert.equal(stored.doi, '10.48550/arxiv.1706.03762', 'real DOI must survive')
    assert.ok(!stored.doi.startsWith('unknown:'), 'DOI must not be a placeholder')
  } finally {
    rmSync(project, { recursive: true, force: true })
  }
})

