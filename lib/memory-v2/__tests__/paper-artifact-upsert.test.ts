/**
 * Tests for upsertPaperArtifact's dedup-merge behavior.
 *
 * Pure file-IO tests — no LLM, no network. Verifies the fill-only
 * semantics introduced by RFC-006 §0 (PR-0): when an upsert hits a
 * dedup target, existing populated fields are preserved instead of
 * being overwritten by thinner incoming data.
 *
 * Regression coverage for the bug where:
 *   - a real DOI was silently downgraded to `unknown:<citeKey>`
 *   - an 11-author list was collapsed to ["First, A.", "others"]
 *   - a curated title was overwritten by a filename-derived string
 *   - the Paper Wiki semantic hash flipped on every re-import
 *
 * Also covers the new `wasDeduped` flag (Q4) and the new
 * `bibtexIsAutoGenerated` schema field (Q7).
 */

import { test } from 'node:test'
import assert from 'node:assert/strict'
import { mkdtempSync, rmSync, mkdirSync } from 'node:fs'
import { tmpdir } from 'node:os'
import { join } from 'node:path'
import { upsertPaperArtifact } from '../../commands/paper-artifact.js'
import { listArtifacts } from '../store.js'
import type { CLIContext, PaperArtifact } from '../../types.js'
import { PATHS } from '../../types.js'

function tmpProject(): string {
  const dir = mkdtempSync(join(tmpdir(), 'pipilot-upsert-'))
  // Ensure papers dir exists so listArtifacts doesn't return [] before any write.
  mkdirSync(join(dir, PATHS.papers), { recursive: true })
  return dir
}

function ctx(projectPath: string): CLIContext {
  return { sessionId: 'test-session', projectPath, debug: false }
}

function getPaperById(projectPath: string, id: string): PaperArtifact {
  const papers = listArtifacts(projectPath, ['paper']).filter(
    (a): a is PaperArtifact => a.type === 'paper'
  )
  const found = papers.find(p => p.id === id)
  if (!found) throw new Error(`paper ${id} not found`)
  return found
}

// ─── Baseline: new paper creation ──────────────────────────────────────────

test('creates a new paper when no dedup match exists', () => {
  const project = tmpProject()
  try {
    const result = upsertPaperArtifact(
      'Attention Is All You Need',
      {
        authors: ['Ashish Vaswani', 'Noam Shazeer'],
        year: 2017,
        doi: '10.48550/arXiv.1706.03762',
        venue: 'NeurIPS'
      },
      ctx(project)
    )

    assert.equal(result.success, true)
    assert.equal(result.wasDeduped, false)
    assert.ok(result.paper)
    assert.equal(result.paper!.title, 'Attention Is All You Need')
    assert.equal(result.paper!.doi, '10.48550/arXiv.1706.03762')
    assert.equal(result.paper!.authors.length, 2)
  } finally {
    rmSync(project, { recursive: true, force: true })
  }
})

